#include <unistd.h>

int	main(int argn, char **argv)
{
	int	i;

	i = 0;
	if (argn <= 0)
		return (0x0);
	while (argv[0][i])
	{
		write(1, &argv[0][i++], 1);
	}
	write(1, &("\n"), 1);
}
